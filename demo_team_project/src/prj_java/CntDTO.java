package prj_java;

public class CntDTO {

	private int cnt_num;
	private String cnt_nick;
	private String cnt_date;
	private String cnt_title;
	private String cnt_content;
	
	
	public int getCnt_num() {
		return cnt_num;
	}
	public void setCnt_num(int cnt_num) {
		this.cnt_num = cnt_num;
	}
	public String getCnt_nick() {
		return cnt_nick;
	}
	public void setCnt_nick(String cnt_nick) {
		this.cnt_nick = cnt_nick;
	}
	public String getCnt_date() {
		return cnt_date;
	}
	public void setCnt_date(String cnt_date) {
		this.cnt_date = cnt_date;
	}
	public String getCnt_title() {
		return cnt_title;
	}
	public void setCnt_title(String cnt_title) {
		this.cnt_title = cnt_title;
	}
	public String getCnt_content() {
		return cnt_content;
	}
	public void setCnt_content(String cnt_content) {
		this.cnt_content = cnt_content;
	}
	
	
	
}
